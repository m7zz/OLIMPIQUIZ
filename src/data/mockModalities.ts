export function getModalidade(id: string): Modalidade | undefined {
  return modalidades.find((m) => m.id === id);
}

export function getModalidadesCard() {
  return modalidades.map(({ id, titulo, descricao, icon }) => ({
    id,
    titulo,
    descricao,
    icon,
  }));
}

 
export function getPerguntasAleatorias(
  modalidadeId: string,
  dificuldade?: Dificuldade
): Pergunta[] {
  const modalidade = modalidades.find((m) => m.id === modalidadeId);
  if (!modalidade) return [];

  const pool = dificuldade
    ? modalidade.perguntas.filter((p) => p.dificuldade === dificuldade)
    : modalidade.perguntas;

  return [...pool].sort(() => Math.random() - 0.5);
}